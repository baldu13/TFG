package model;

public class TipoExperimento {

	//Atributos
	private int id;
	private String tipo;
	
	//Metodos
	public TipoExperimento() {}

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}

	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
}
